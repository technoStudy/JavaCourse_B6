package Day3.PrimitiveTypes;

public class LongDataType {

    public static void main(String[] args) {

        long priceForTwitter = 44_000_000_000L; // at the end of number we should put "L"

        long myFirstLong = 58_965_821_432L;
        long mySecondLong = -86_424_322_098L;

        System.out.println(myFirstLong);
        System.out.println(mySecondLong);

        long maxValue = 923_372_036_854_775_807L;
        long minValue = -923_372_036_854_775_808L;

        System.out.println(maxValue);
        System.out.println(minValue);

    }

}
