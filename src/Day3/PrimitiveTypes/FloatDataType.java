package Day3.PrimitiveTypes;

public class FloatDataType {

    public static void main(String[] args) {

        float laptopPrice = 999.98F;
        System.out.println(laptopPrice);

        float negativeDecimalPointNumber = -79.54F;
        System.out.println(negativeDecimalPointNumber);

        float x = 5F;
        System.out.println(x);

    }

}
